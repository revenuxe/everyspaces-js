<h2 id="faqs">Frequently Asked Questions</h2>

<h3>What is the cost of a modular wardrobe in Bangalore?</h3>
<p>A modular wardrobe in Bangalore costs between <strong>₹35,000 and ₹2,80,000</strong> depending on size, material, and finish. A standard 6-foot couple''s wardrobe in BWR plywood with laminate finish costs approximately ₹80,000–₹1,30,000 including hardware and installation.</p>

<h3>Which wardrobe material is best for Bangalore climate?</h3>
<p><strong>BWR (Boiling Water Resistant) plywood with laminate finish</strong> is the best overall choice for Bangalore. It handles the monsoon humidity well and lasts 15–20 years. Avoid particle board — it swells and warps in Bangalore''s climate within 2–3 years.</p>

<h3>How long does it take to build a custom wardrobe?</h3>
<p>At Intorza, a single wardrobe takes <strong>12–18 days</strong> from design approval to installation. A full home (3 bedrooms) takes about 20–25 days. Factory production happens simultaneously, so more wardrobes doesn''t proportionally increase time.</p>

<h3>Sliding wardrobe vs hinged wardrobe — which is better?</h3>
<p><strong>Sliding wardrobes are better for most Bangalore apartments</strong> because they don''t need door-swing clearance (saving 2.5 feet of space). Hinged wardrobes are better for large bedrooms where you want full access to the wardrobe at once. Sliding doors also look more modern and sleek.</p>

